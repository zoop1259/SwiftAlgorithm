import UIKit

func solution(_ n : Int ) -> Int {
    var arr = [Int](repeating : 0, count : n+2)
    arr[1] = 1
    arr[2] = 2
    if n >= 3 {
        for i in 3...n {
            arr[i] = ( arr[i-2] + arr[i-1] ) % 1234567
        }
    }
    return arr[n]
}

